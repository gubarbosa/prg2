comida = {
    'batata': 1, 'frango': 2, 'arroz': 3, 'alface': 4, 'feijao': 5, 'trigo': 6, 'farofa': 7
}

print(comida.keys())   #keys determinam as chaves de um dicionário em Python, como: 'batata', 'frango', 'arroz'..
print(comida.values())  #values determinam os valores das chaves em um dicionário no Python, como: 1, 2, 3, 4...
print(comida.items())  #items explora tanto as chaves e seus respectivos valores em um dicionário do Python.